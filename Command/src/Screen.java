public class Screen {
    public void screenUp() {
        System.out.println("Screen is up");
    }
    public void screenDown() {
        System.out.println("Screen is down");
    }
    public void screenBreak() {
        System.out.println("Screen is broken");
    }

    public void screenFix() {
        System.out.println("Screen is fixed");
    }
}
